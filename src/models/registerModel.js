const mongoose = require('mongoose');
// const validator = require('validator');

const RegisterSchema = mongoose.Schema({
  fullName: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  }
});

module.exports = mongoose.model('accounts', RegisterSchema);